# Exercício de cálculo da área de um quadrado

#10) Escreva um algoritmo para ler as dimensões de um quadrado (lado), calcular e escrever a área do quadrado. Dica a=l²

print("Vamos ler as dimensões do quadrado e exibir a sua área em m²")

base = float(input("Digite a base do quadrado em m²: "))
altura = float(input("Digite a altura do quadrado em m²: "))

area = base * altura

print("A área do quadrado é:", area,"m²")